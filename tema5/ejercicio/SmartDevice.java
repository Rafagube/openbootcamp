package tema5.ejercicio;

public class SmartDevice {

	//Atributos
	
	String modelo;
	String fabricante;
	double versionSO;
	double coste;
	
	
	public SmartDevice() {
		
	}
	
	public SmartDevice(String modelo, String fabricante, double versionSO, double coste) {
		this.modelo = modelo;
		this.fabricante = fabricante;
		this.versionSO = versionSO;
		this.coste = coste;
		
		
	}
	

}